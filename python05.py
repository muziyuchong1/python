def Sort(list):
    n = len(list)
    for i in range(1, n):
        for j in range(1, n - i + 1):
            if list[j - 1] > list[j]:
                list[j - 1], list[j] = list[j], list[j - 1]
            # 打印排序过程
            print(list)
    for i in range(0, n):
        print(list[i])
